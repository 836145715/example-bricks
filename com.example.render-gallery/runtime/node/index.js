async function registerResource(filePath, mimeType, name) {
  return sendHostCall('host.resource.register', {
    resource: {
      filePath,
      mimeType,
      name,
      size: fs.statSync(filePath).size,
      ttlMs: 30 * 60_000
    }
  })
}

