function svgDataUrl(title) {
  return `data:image/svg+xml;base64,${Buffer.from(svgString(title), 'utf8').toString('base64')}`
}

function writeSvgFile(dir, title) {
  const filePath = path.join(dir, 'render-gallery-image.svg')
  fs.writeFileSync(filePath, svgString(title), 'utf8')
  return filePath
}

function writeWavFile(dir) {
  const filePath = path.join(dir, 'render-gallery-tone.wav')
  const sampleRate = 8000
  const seconds = 1
  const samples = sampleRate * seconds
  const dataSize = samples * 2
  const buffer = Buffer.alloc(44 + dataSize)
  buffer.write('RIFF', 0)
  buffer.writeUInt32LE(36 + dataSize, 4)
  buffer.write('WAVE', 8)
  buffer.write('fmt ', 12)
  buffer.writeUInt32LE(16, 16)
  buffer.writeUInt16LE(1, 20)
  buffer.writeUInt16LE(1, 22)
  buffer.writeUInt32LE(sampleRate, 24)
  buffer.writeUInt32LE(sampleRate * 2, 28)
  buffer.writeUInt16LE(2, 32)
  buffer.writeUInt16LE(16, 34)
  buffer.write('data', 36)
  buffer.writeUInt32LE(dataSize, 40)
  for (let i = 0; i < samples; i += 1) {
    const t = i / sampleRate
    const value = Math.round(Math.sin(2 * Math.PI * 440 * t) * 0.25 * 32767)
    buffer.writeInt16LE(value, 44 + i * 2)
  }
  fs.writeFileSync(filePath, buffer)
  return filePath
}

